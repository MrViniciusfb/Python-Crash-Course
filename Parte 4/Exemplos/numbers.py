#A função range() facilita  gerar uma série de números
	#Esta função não conta o último número, portanto se quiser mostrá-lo deve-se somar um no último
for value in range(1,6):
	print(value)

#É possível tbm usar o range pare gerar listas com a função list()
numbers = list(range(1,6))
print(numbers)

